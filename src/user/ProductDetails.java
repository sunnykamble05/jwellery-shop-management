 public void loadproductData() {
       
        try {
             Class.forName("com.mysql.cj.jdbc.Driver");
              java.sql.Connection con= java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","sunny123");
             
              PreparedStatement pstmt = con.prepareStatement("SELECT * FROM product");
             
           ResultSet rs = pstmt.executeQuery();
           
            DefaultTableModel model = (DefaultTableModel)ProductTable.getModel();
            model.setRowCount(0); // Clear previous data

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("product_id"),
                    rs.getString("product_name"),
                    rs.getString("product_price"),
                     rs.getString("weight"),
                   
                   
                        
                });
            }
            rs.close();
        pstmt.close();
        con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to load product data: " + e.getMessage());
        }
    }

    
